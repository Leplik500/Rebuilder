namespace User.Domain.Enum;

/// <summary>
/// Represents the visual theme preference selected by the user for the application's interface.
/// </summary>
public enum Theme
{
    /// <summary>
    /// Dark theme: optimized for low-light environments with dark background and light text.
    /// </summary>
    Dark,

    /// <summary>
    /// Light theme: optimized for well-lit environments with light background and dark text.
    /// </summary>
    Light,
}
